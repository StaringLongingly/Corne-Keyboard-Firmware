static void update_last_typed(void) {
    last_typed_array[top] = key_name;
    top = (top + 1) % LAST_TYPED_ARRAY_SIZE;
}

static void render_last_typed(void) {
    for (uint8_t i = 0; i < LAST_TYPED_ARRAY_SIZE; i++) {
        uint8_t index = (top + i) % LAST_TYPED_ARRAY_SIZE;
        oled_write_char(last_typed_array[index], false);
    }
}

